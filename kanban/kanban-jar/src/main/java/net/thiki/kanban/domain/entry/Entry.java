package net.thiki.kanban.domain.entry;

public class Entry {
    
    private Integer id;

    private String title;

    public Entry(String title) {
        this.title = title;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}